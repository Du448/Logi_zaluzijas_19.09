function resolveGrid(material?: string): RomiesuGrid | undefined {
  if (material) {
    const override = materialGridOverrides[material]
    if (override) {
      return override
    }
    if (!defaultGridMaterials.includes(material)) {
      return undefined
    }
  }
  return defaultGrid
}

export function getTariffSizeCm(
  material: string | undefined,
  widthMm: number,
  heightMm: number,
): { widthCm: number; heightCm: number } | undefined {
  const grid = resolveGrid(material)
  if (!grid || grid.widthsCm.length === 0 || grid.heightsCm.length === 0) return undefined
  const w = Math.ceil((widthMm / 10) / 10) * 10
  const h = Math.ceil((heightMm / 10) / 10) * 10
  const minW = Math.min(...grid.widthsCm)
  const maxW = Math.max(...grid.widthsCm)
  const minH = Math.min(...grid.heightsCm)
  const maxH = Math.max(...grid.heightsCm)
  const widthCm = Math.max(minW, Math.min(w, maxW))
  const heightCm = Math.max(minH, Math.min(h, maxH))
  return { widthCm, heightCm }
}

export function getGridPrice(material: string | undefined, widthMm: number, heightMm: number): number | undefined {
  const grid = resolveGrid(material)
  if (!grid || grid.widthsCm.length === 0 || grid.heightsCm.length === 0 || grid.prices.length === 0) return undefined
  const size = getTariffSizeCm(material, widthMm, heightMm)
  if (!size) return undefined
  const { widthCm, heightCm } = size
  const wIdx = grid.widthsCm.indexOf(widthCm)
  const hIdx = grid.heightsCm.indexOf(heightCm)
  if (wIdx < 0 || hIdx < 0) return undefined
  const row = grid.prices[hIdx]
  if (!row) return undefined
  const cappedRow = row.length > grid.widthsCm.length ? row.slice(0, grid.widthsCm.length) : row
  const value = cappedRow[wIdx]
  return typeof value === "number" ? value : undefined
}
